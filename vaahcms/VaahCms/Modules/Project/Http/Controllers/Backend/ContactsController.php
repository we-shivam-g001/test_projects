    public function getAssets(Request $request)
    {

        try{

            $data = [];

            $data['permission'] = [];
            $data['rows'] = config('vaahcms.per_page');
            $data['language_strings'] = [
                "companies_title" => trans("vaahcms-projects.company_title"),
                "contacts_title" => trans("vaahcms-projects.contacts_title")


            ];
            $data['fillable']['columns'] = Contact::getFillableColumns();
            $data['fillable']['except'] = Contact::getUnFillableColumns();
            $data['empty_item'] = Contact::getEmptyItem();
            $data['empty_item_company'] = Company::getEmptyItem();
//            $data['countries'] = array_column(VaahCountry::getList(), 'name');
            $data['countries'] = vh_get_country_list();

            $data['actions'] = [];

            $response['success'] = true;
            $response['data'] = $data;

        }catch (\Exception $e){
            $response = [];
            $response['success'] = false;
            if(env('APP_DEBUG')){
                $response['errors'][] = $e->getMessage();
                $response['hint'] = $e->getTrace();
            } else{
                $response['errors'][] = trans("vaahcms-general.something_went_wrong");
            }
        }

        return $response;
    }

